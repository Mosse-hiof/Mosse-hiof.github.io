const bodyLocks = new Set();
let currentLang = "en";
let lastFocusedBeforeContact = null;

function getLang() {
  return (document.documentElement.lang || currentLang || "en").toLowerCase().startsWith("no") ? "no" : "en";
}

function syncBodyLock() {
  document.body.style.overflow = bodyLocks.size ? "hidden" : "";
}

function lockBody(source) {
  bodyLocks.add(source);
  syncBodyLock();
}

function unlockBody(source) {
  bodyLocks.delete(source);
  syncBodyLock();
}

function getFocusableElements(container) {
  return Array.from(container.querySelectorAll(
    'a[href], button:not([disabled]), input:not([disabled]):not([type="hidden"]), textarea:not([disabled]), select:not([disabled]), [tabindex]:not([tabindex="-1"])'
  )).filter(el => !el.hasAttribute("hidden") && el.getClientRects().length > 0);
}

function setLang(lang) {
  currentLang = translations[lang] ? lang : "en";
  const t = translations[currentLang];

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  document.documentElement.lang = currentLang === "no" ? "no" : "en";
  document.querySelectorAll("[data-lang-toggle]").forEach(el => {
    el.textContent = currentLang === "no" ? "NO" : "EN";
  });

  try {
    localStorage.setItem("digitekt-lang", currentLang);
  } catch (error) {
    // Ignore storage restrictions.
  }
}

function toggleLang() {
  setLang(getLang() === "en" ? "no" : "en");
}

function toggleMobileMenu() {
  const menu = document.getElementById("mobile-menu");
  const icon = document.getElementById("burger-icon");
  const toggle = document.getElementById("mobile-nav-toggle");
  if (!menu || !icon) return;

  const isOpen = menu.classList.toggle("open");
  menu.setAttribute("aria-hidden", String(!isOpen));
  if (isOpen) {
    menu.removeAttribute("inert");
    lockBody("mobile-menu");
  } else {
    menu.setAttribute("inert", "");
    unlockBody("mobile-menu");
  }

  if (toggle) {
    toggle.setAttribute("aria-expanded", String(isOpen));
    toggle.setAttribute("aria-label", isOpen ? "Close menu" : "Open menu");
  }

  icon.innerHTML = isOpen
    ? '<path d="M5 5L17 17M5 17L17 5" stroke="currentColor" stroke-width="1.5" stroke-linecap="round"/>'
    : '<path d="M3 7H19M3 15H19" stroke="currentColor" stroke-width="1.5" stroke-linecap="round"/>';
}

function closeMobileMenu() {
  const menu = document.getElementById("mobile-menu");
  if (menu && menu.classList.contains("open")) toggleMobileMenu();
}

function openContact() {
  const overlay = document.getElementById("contact-overlay");
  if (!overlay) return;

  closeMobileMenu();
  lastFocusedBeforeContact = document.activeElement;
  overlay.classList.add("open");
  overlay.setAttribute("aria-hidden", "false");
  overlay.removeAttribute("inert");
  lockBody("contact");

  setTimeout(() => {
    const firstFocusable = getFocusableElements(overlay)[0];
    if (firstFocusable) firstFocusable.focus();
  }, 0);
}

function closeContact() {
  const overlay = document.getElementById("contact-overlay");
  if (!overlay || !overlay.classList.contains("open")) return;

  overlay.classList.remove("open");
  overlay.setAttribute("aria-hidden", "true");
  overlay.setAttribute("inert", "");
  unlockBody("contact");

  if (
    lastFocusedBeforeContact &&
    typeof lastFocusedBeforeContact.focus === "function" &&
    document.contains(lastFocusedBeforeContact) &&
    lastFocusedBeforeContact.getClientRects().length > 0
  ) {
    lastFocusedBeforeContact.focus({ preventScroll: true });
  }
  lastFocusedBeforeContact = null;
}

function formHasUserInput(form) {
  return Array.from(form.elements).some((el) => {
    if (!el.name || el.type === "hidden" || el.type === "submit" || el.name === "bot-field") return false;
    return typeof el.value === "string" && el.value.trim() !== "";
  });
}

function trapContactFocus(e) {
  const overlay = document.getElementById("contact-overlay");
  if (!overlay || !overlay.classList.contains("open")) return;

  const focusable = getFocusableElements(overlay);
  if (!focusable.length) return;

  const first = focusable[0];
  const last = focusable[focusable.length - 1];

  if (e.shiftKey && document.activeElement === first) {
    e.preventDefault();
    last.focus();
  } else if (!e.shiftKey && document.activeElement === last) {
    e.preventDefault();
    first.focus();
  }
}

function handleSubmit(e) {
  e.preventDefault();
  const form = document.getElementById("contact-form");
  if (!form) return;

  const submitBtn = form.querySelector('button[type="submit"]');
  const originalBtnHTML = submitBtn.innerHTML;
  submitBtn.disabled = true;
  submitBtn.textContent = getLang() === "no" ? "Sender..." : "Sending...";

  const formData = new FormData(form);
  const params = new URLSearchParams();
  formData.forEach((value, key) => params.append(key, value));

  fetch("/", {
    method: "POST",
    headers: { "Content-Type": "application/x-www-form-urlencoded" },
    body: params.toString(),
  })
    .then((response) => {
      if (!response.ok) throw new Error("Network response was not ok");
      form.classList.add("hidden");
      document.getElementById("form-success").classList.remove("hidden");
      setTimeout(() => {
        form.classList.remove("hidden");
        document.getElementById("form-success").classList.add("hidden");
        form.reset();
        submitBtn.disabled = false;
        submitBtn.innerHTML = originalBtnHTML;
        closeContact();
      }, 3500);
    })
    .catch((err) => {
      console.error("Form submission failed:", err);
      submitBtn.disabled = false;
      submitBtn.innerHTML = originalBtnHTML;
      alert("Sorry - something went wrong sending your message. Please email us directly at info@digitekt.no.");
    });
}

function initRevealObserver() {
  const reveals = document.querySelectorAll(".reveal:not(.active)");
  if (!("IntersectionObserver" in window)) {
    reveals.forEach(el => el.classList.add("active"));
    return;
  }

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add("active");
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.15, rootMargin: "0px 0px -50px 0px" });

  reveals.forEach(el => observer.observe(el));
}

function initMagneticButtons() {
  document.querySelectorAll(".magnetic-btn").forEach(btn => {
    btn.addEventListener("mousemove", (e) => {
      const rect = btn.getBoundingClientRect();
      const x = e.clientX - rect.left - rect.width / 2;
      const y = e.clientY - rect.top - rect.height / 2;
      btn.style.transform = "translate(" + (x * 0.3) + "px, " + (y * 0.3) + "px)";
    });

    btn.addEventListener("mouseleave", () => {
      btn.style.transform = "translate(0, 0)";
    });
  });
}

